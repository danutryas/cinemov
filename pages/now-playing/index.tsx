import { Header } from "@/components";
import { MovieCard } from "@/components/card/card";
import Head from "next/head";

// list of movie
export async function getServerSideProps() {
  // Fetch data from external API
  const res = await fetch(`https://seleksi-sea-2023.vercel.app/api/movies`);
  const data = await res.json();

  return { props: { data } };
}

interface NowPlayingProps {
  data: any;
}

const NowPlaying = (props: NowPlayingProps) => {
  const { data } = props;
  console.log(data);
  return (
    <>
      <Head>
        <title>Now Playing | cinemov</title>
        <meta name="description" content="Generated by Create Next Stack." />
      </Head>
      <div className="grid gap-5 truncate grid-cols-4 p-5">
        {data.map((movie: any, id: number) => (
          <MovieCard
            key={id}
            imgUrl={movie.poster_url}
            title={movie.title}
            id={id.toString()}
          />
        ))}
      </div>
    </>
  );
};
export default NowPlaying;
